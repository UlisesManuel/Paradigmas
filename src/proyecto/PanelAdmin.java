    private void btnEditComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditComidaActionPerformed
        comidaList.get(i).id=(int)nuevoID.getValue();
        comidaList.get(i).nombre=nuevoNombre.getText();
        comidaList.get(i).precio=(double)nuevoPrecio.getValue();
        comidaList.get(i).tipo=nuevoTipo.getSelectedItem().toString();
        bnombre.setText("");
        nuevoID.setValue(0);
        nuevoNombre.setText("");
        nuevoPrecio.setValue(0);
        j=-1;
        
        
    }//GEN-LAST:event_btnEditComidaActionPerformed

    private void buscarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbtnActionPerformed
        j=validarComida(bnombre.getText());
        if(j!=-1){
            buscarcont.setVisible(true);
            nuevoID.setValue(comidaList.get(j).id);
            nuevoNombre.setText(comidaList.get(j).nombre);
            nuevoPrecio.setValue(comidaList.get(j).precio);
        }else{
            JOptionPane.showMessageDialog(this, "Comida no encontrada");
        }
    }//GEN-LAST:event_buscarbtnActionPerformed

