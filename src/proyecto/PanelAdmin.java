    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        i=validarRegistro(person.getText());
        if(i!=-1){
            editNombre.setText(UsuariosLista.get(i).NombreCompleto);
            editUser.setText(UsuariosLista.get(i).Usuario);
            editContra.setText(UsuariosLista.get(i).Contraseña);
            contEdit.setVisible(true);
            
        }else{
            JOptionPane.showMessageDialog(this, "Usuario no encontrado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

